public class Tutorial_03_variables_and_Datatypes_in_java {
    public static void main(String[] args){
        //Java Variables syntax: datatype variableName = value;
        String name="waqas khan";
        int regNo=1141;

        System.out.println("My name is : "+name);
        System.out.println("My Registration Number is : "+regNo);

        byte byteNumber=127;                //byte: The main purpose of byte is used to save memory and consists values in the range -128 to 127.
        short shortNumber=5678;             //short: consists values in the range -32768 to 32767.
        int intNumber=1234;                 //int: consists values in the range -2,147,483,648 to 2,147,483,647.
        long longNumber=345678789;          //long: consists values in the range -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        char singleAlphabet='A';            //char datatype is used to store characters.
        float floatingNumber=1.3245f;       //float: can be used to deal with decimal numbers. Always recommended to use float rather than double because float saves memory.
        double doubleNumber=123456.456d;    //double: can be used to deal with decimal numbers.
        boolean isTure=true;                //Boolean data type consists of true and false values.
        String codingIn="java";             //in java string is not a primitive data type it is a class and used for String literals
    }
}
