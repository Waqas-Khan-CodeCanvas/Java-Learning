public class Tutorial_02_basic_Structure_of_java_programe {
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}
//Basic understanding
//Let's break down this basic Java program:
//the first line declare java class which is explained below:-
    //public: This is an access modifier, which means the class is accessible by any other class.
    //class: This keyword defines a class in Java. A class is a blueprint for creating objects.
    //tutorial_01_introduction_to_java: This is the name of the class.
    // By convention, class names should begin with a capital letter, although this is not mandatory in Java.
    //{}: curly brakets represents block or scope of specific code

//2. Main Method: second line inside the public class is the main method
    //public: This means the method can be called from anywhere, outside of the class as well.
    //static: This means the method belongs to the class, not to a specific instance of the class. This allows Java to call the main method without creating an object of the class.
    //void: This means the method does not return any value.
    //main: This is the name of the method. The main method is the entry point of any Java application.
    //String[] args: This is an array of String values pass as a parameter . It can be used to pass external data to the program.

//3. Method Body: inside the the curly braces of the main method called method body in which we can write our code
    //System.out: System is a class, and out is a static field in that class representing the standard output stream (typically the console).
    //println: This is a method that prints the argument passed to it and moves the cursor to the next line.
    //"Hello World!": This is the string to be printed on the console.

//4. Flow of programe:
    //The program starts executing from the main method. and then continue according to the code

