public class QuestionNo_18 {
    public static void main(String[] args) {
        System.out.println("Temperature Converter (Celsius to Fahrenheit)");
        double tempInCelsius = 27.6 ;
        double fahranheit = (tempInCelsius * 9/5) + 32 ; //Fahrenheit = (Celsius * 9/5) + 32.

        System.out.println("Celsius : "+ tempInCelsius);
        System.out.println("Fahrenheit : "+ fahranheit);
    }
}
