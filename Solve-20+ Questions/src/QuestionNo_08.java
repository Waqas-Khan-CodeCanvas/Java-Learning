public class QuestionNo_08 {
    public static void main(String[] args) { //Working with Long and Double
        long longValue = 10000000000L;// we have to add L because by default type is double in java
        double doubleValue = 98957.9345678234D ;
        System.out.println("Long value : "+longValue);
        System.out.println("Double value : "+doubleValue);
    }
}
